# Step 1: Make all the "turtle" commands available to us.
import turtle

# Step 2: Create a new turtle. We'll call it "bob"
bob = turtle.Turtle()

# Step 3: Move in the direction Bob's facing for 300 pixels
bob.forward(300)
#bob.backward(300)

# Step 4: We're done!
turtle.done()