# Multiple Turtle

# You can make more than one turtle. Just give them different names! You can then make them do different things
# or give them different colors.

import turtle

pooja=turtle.Turtle()
gaurav=turtle.Turtle()

pooja.color("Red")
gaurav.color("Blue")

pooja.forward(100)

gaurav.left(40)
gaurav.forward(100)

turtle.done()
